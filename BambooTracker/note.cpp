uint16_t calculateFNumber(int absPitch, int finePitch)
{
	uint16_t p = centTableFM[absPitch];
	return (finePitch ? (finePitch > 0 ? p + finePitch : p - static_cast<uint16_t>(-finePitch))
					  : p);
}

